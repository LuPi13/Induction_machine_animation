"""
단상 유도전동기 파라미터
"""

# 전원 파라미터
V_s = 220           # 공급 전압 (V, rms)
f = 60              # 주파수 (Hz)
omega = 2 * 3.14159265 * f  # 각주파수 (rad/s)

# 스테이터 파라미터
R_s = 2.0           # 스테이터 저항 (Ω)
L_s = 0.01          # 스테이터 인덕턴스 (H)
X_s = omega * L_s   # 스테이터 리액턴스 (Ω)

# 자화 및 철손
L_m = 0.15          # 자화 인덕턴스 (H)
X_m = omega * L_m   # 자화 리액턴스 (Ω)
R_c = 500           # 철손 저항 (Ω)

# 로터 파라미터
R_r = 3.0           # 로터 저항 (Ω)
L_r = 0.01          # 로터 인덕턴스 (H)
X_r = omega * L_r   # 로터 리액턴스 (Ω)

# 기계적 파라미터
P = 2               # 극 수
n_sync = 120 * f / P  # 동기속도 (rpm)

# 시뮬레이션 파라미터
n_rotor_bars = 6    # 로터 도선 개수 (시각화용)
